package com.com.nidaizi.mimall2;

public class If_else {
    public static void main(String[] args) {
        int a=59;
        if (a>=90){
            System.out.println("优秀");
        }else if (a>=80){
            System.out.println("良好");
        }else if (a>=70){
            System.out.println("中等");
        }else if (a>=60){
            System.out.println("及格");
        }else {
            System.out.println("不及格");
        }
    }
}
